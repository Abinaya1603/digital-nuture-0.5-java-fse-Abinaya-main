public class DataTypesDemo {
    public static void main(String[] args) {
        int num = 10;
        float f = 3.14f;
        double d = 2.71828;
        char c = 'A';
        boolean b = true;
        System.out.println(num);
        System.out.println(f);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);
    }
}